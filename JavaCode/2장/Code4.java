package code4;

public class Code4 {
	public static void main(String[] args)
	{
		int a = 100;
		int b = 250;
		System.out.println(a); // 변수 a의 값 출력
		System.out.println(b); // 변수 b의 값 출력
		System.out.println(a + b); // a와 b를 더한 결과 출력
		System.out.println("--------------");
		System.out.println("a"); // 쌍따옴표 안의 문자를 그대로 출력
		System.out.println("a + b"); // 쌍따옴표 안의 문자를 그대로 출력
	}
}
